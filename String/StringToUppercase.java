//Write a Java program to convert all the characters in a string to uppercase

public class StringToUppercase
{
	public static void main (String [] args)
	{
		String st1 = "The Quick BroWn FoX!";
		String st2 = st1.toUpperCase();
		
		System.out.println("Original String: " + st1);
		System.out.println("String in uppercase: " + st2);
		
		
	}
}



